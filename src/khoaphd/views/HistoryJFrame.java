    private void tblBillMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBillMouseClicked
        int row = tblBill.getSelectedRow();
        
        int billID = (int)tblBill.getValueAt(row, 0);
        BillDTO billDTO = null;
        try {
            setModelForBookByBillID(billID);
            billDTO = billDAO.getBillByID(billID);
        } catch (Exception e) {
            e.printStackTrace();
        }
        txtTotal.setText(Double.toString(billDTO.getTotalAll()));
        txtDiscountID.setText(billDTO.getDiscountID());
        txtTotalDiscount.setText(Double.toString(billDTO.getTotalDiscount()));
    }//GEN-LAST:event_tblBillMouseClicked

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String searchStr = txtSearch.getText().trim();
        if (searchStr.isEmpty()) {
            try {
                setModelForBill();
            } catch (Exception e) {
                e.printStackTrace();
            }
            return;
        }
        if (!searchStr.matches("^\\d{4}-\\d{2}-\\d{2}$")) {
            JOptionPane.showMessageDialog(this, "Date must follow the format YYYY-MM-DD");
            txtSearch.requestFocus();
            return;
        }
        
        Date searchDate = null;
        try {
            searchDate = convertStringToDate(searchStr);
            setModelForBillToSearch(searchDate);
            modelBook.setRowCount(0);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Invalid date");
            txtSearch.requestFocus();
            return;
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

