    private void setModelForBill() throws Exception {
        modelBill.setRowCount(0);
        Vector<BillDTO> list = billDAO.getBillsByUser(userID);
        for (BillDTO dto : list) {
            modelBill.addRow(dto.toVector());
        }
    }
    
    private void setModelForBillToSearch(Date buyDate) throws Exception {
        modelBill.setRowCount(0);
        Vector<BillDTO> list = billDAO.getBillsByBuyDate(userID, buyDate);
        for (BillDTO dto : list) {
            modelBill.addRow(dto.toVector());
        }
    }

    private void setModelForBookByBillID(int billID) throws Exception {
        modelBook.setRowCount(0);
        Vector<BookInBillDTO> list = bookInBillDAO.getBooksByBill(billID);
        for (BookInBillDTO dto : list) {
            modelBook.addRow(dto.toVector());
        }
    }
    
    private void disableFields() {
        txtTotal.setEditable(false);
        txtDiscountID.setEditable(false);
        txtTotalDiscount.setEditable(false);
    }
    
    private Date convertStringToDate(String dateStr) throws ParseException {
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        dateFormat.setLenient(false);
        java.util.Date date = dateFormat.parse(dateStr);
        Date sqlDate = new Date(date.getTime());
        return sqlDate;
    }
    
